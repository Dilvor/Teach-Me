package homeWork.homeWork10;

public class Automobile {
    int number;//Номер автомобиля
    int speed;//Скорость автомобиля
    int weight;//Вес в КГ
    double width;//Ширина в метрах
    double height;//Высота в метрах

    public Automobile(int number, int speed, int weight, double width, double height) {
        this.number = number;
        this.speed = speed;
        this.weight = weight;
        this.width = width;
        this.height = height;
    }
}
